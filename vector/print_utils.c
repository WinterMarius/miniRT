#include "vecmath.h"
#include <stdio.h>

void    print_matrix(t_matrix4x4 m)
{
    printf("%10f  %10f  %10f  %10f\n", m.m[0][0], m.m[0][1], m.m[0][2], m.m[0][3]);
    printf("%10f  %10f  %10f  %10f\n", m.m[1][0], m.m[1][1], m.m[1][2], m.m[1][3]);
    printf("%10f  %10f  %10f  %10f\n", m.m[2][0], m.m[2][1], m.m[2][2], m.m[2][3]);
    printf("%10f  %10f  %10f  %10f\n", m.m[3][0], m.m[3][1], m.m[3][2], m.m[3][3]);
}

void    print_vec3(t_vec3 v)
{
    printf("%10f  ", v.x);
    printf("%10f  ", v.y);
    printf("%10f\n", v.z);
}